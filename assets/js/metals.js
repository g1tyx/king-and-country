function forgeMaterial(metal, material, button) {
  if(material.cost <= metal.total && !material.forged){
    metal.total = metal.total-material.cost;
    material.forged = true;
    button.textContent = "Forged!";
    button.disabled = true;
    addStats(metal, material);
    formatText();
  }
}


function metalPurchases(armor, metal, armorClickID) {
  if (armor.forged) {
    armorClickID.disabled = true;
    armorClickID.textContent = "Forged!";
  }
  else if (metal.total >= armor.cost) {
    armorClickID.disabled = false;
  }
  else{
    armorClickID.disabled = true;
  }
}
